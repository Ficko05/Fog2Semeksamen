package FunctionLayer;

class Carport {

    private int stopleL, stopleW, rem, spær;

    public Carport(int stopleL, int stopleW, int rem, int spær) {
        this.stopleL = stopleL;
        this.stopleW = stopleW;
        this.rem = rem;
        this.spær = spær;
    }

    public int getStopleL() {
        return stopleL;
    }

    public int getStopleW() {
        return stopleW;
    }

    public int getRem() {
        return rem;
    }

    public int getSpær() {
        return spær;
    }

}
