package FunctionLayer;

class Carport {

    private int stolpeL, stolpeW, rem, spær;

    public Carport(int stolpeL, int stolpeW, int rem, int spær) {
        this.stolpeL = stolpeL;
        this.stolpeW = stolpeW;
        this.rem = rem;
        this.spær = spær;
    }

    public int getStolpeL() {
        return stolpeL;
    }

    public int getStolpeW() {
        return stolpeW;
    }

    public int getRem() {
        return rem;
    }

    public int getSpær() {
        return spær;
    }

}
